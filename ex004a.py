n = (input('Digite algo. '))
print(n)
# .isnumeric() indica bolleano se é numero
print('Você digitou algo númerico', n.isnumeric())
# .isalpha() indica bolleano se é letra
print('Você digitou letras', n.isalpha())
# .isalnum() indica bolleano se é alfanumérico
print('Você digitou alfanumérico', n.isalnum())